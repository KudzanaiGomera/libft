#include "libft.h"

size_t	ft_count(char const *s, char c)
{
	size_t	count;

	count = 0;
	while (s && *s != '\0')
	{
		if (s && *s != c && *s != '\0')
			count++;
		while (s && *s != c && *s != '\0')
			s++;
		s++;
	}
	return (count);
}
