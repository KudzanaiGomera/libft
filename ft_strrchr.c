#include "libft.h"

char				*ft_strrchr(const char *s1, int n)
{
	int c;

	c = ft_strlen(s1);
	while (c >= 0)
	{
		if (s1[c] == n)
			return ((char *)&s1[c]);
		c--;
	}
	return (NULL);
}
