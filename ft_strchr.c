#include "libft.h"

char			*ft_strchr(const char *s1, int n)
{
	while (*s1)
	{
		if (*s1 == (char)n)
			return (char *)s1;
		s1++;
	}
	if (n == 0)
		return (char *)s1;
	return (NULL);
}
